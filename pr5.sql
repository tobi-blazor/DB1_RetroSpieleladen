AS
SELECT Nachname
FROM Kunde
WHERE FLOOR((SYSDATE - Geburtsdatum) / 365) > 35;

SELECT *
FROM KUNDE
WHERE ORT NOT LIKE '%Maieskuel%'
  AND EXTRACT(YEAR FROM GEBURTSDATUM) > (EXTRACT(YEAR FROM SYSDATE) - 35)
ORDER BY NACHNAME DESC

