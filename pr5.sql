FROM KUNDE
         LEFT JOIN test t on KUNDE.NACHNAME = t.Nachname
WHERE ORT LIKE 'Maieskuel%'
  AND (EXTRACT(YEAR FROM GEBURTSDATUM) < (EXTRACT(YEAR FROM SYSDATE) - 35))

